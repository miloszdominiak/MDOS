static inline uint8_t color(enum vga_color bg, enum vga_color fg)
{
    return COLOR(bg, fg);
}

inline void set_color(enum vga_color bg, enum vga_color fg)
{
    terminal_color = COLOR(bg, fg);
	terminal_color = 0xAAAAAA;
}

void put_pixel(int x, int y, uint32_t color)
{
    vesa_buffer[y * tscreen_width + x] = color; 
}

void put_character_at(uint8_t character, uint32_t color, uint8_t char_y, uint8_t char_x)
{
    // for(int y = 0; y < 16; y++)
    //     for(int x = 0; x < 8; x++)
	// 		if(font[character][y] & (1 << (7 - x)))
    //         	put_pixel(char_x * 8 + x, char_y * 16 + y, color);
	// 		else
	// 			put_pixel(char_x * 8 + x, char_y * 16 + y, 0);

    color += 1;
    uint32_t* wskaznik = vesa_buffer + (char_y * 16) * tscreen_width + (char_x * 8);
    for(int y = 0; y < 16; y++)
    {
        for(int x = 0; x < 8; x++)
        {
            wskaznik[x] = 0xFFFFFF & maska[font[character][y]][x];
        }
        wskaznik += tscreen_width;
    }

    tscreen_buffer[char_y][char_x] = character;
}

void set_cursor(uint8_t y, uint8_t x)
{
    put_character_at(0xFE, terminal_color, y, x);
}

void terminal_initialize(uint32_t buffer, uint32_t width, uint32_t height)
{   
    terminal_echo = true;
    vesa_buffer = (uint32_t*)buffer;

	tscreen_width = width;
	tscreen_height = height;

	terminal_columns = tscreen_width / 8;
	terminal_rows = tscreen_height / 16;
    
    for(uint8_t y = 0; y < terminal_rows; y++)
        for(uint8_t x = 0; x < terminal_columns; x++)
            put_character_at(' ', terminal_color, y, x);
    
    terminal_row = terminal_column = 0;
}

void update_cursor()
{
    set_cursor(terminal_row, terminal_column);
}

char get_character(size_t x, size_t y)
{
    return tscreen_buffer[y][x];
}

void terminal_scroll()
{
    for(uint32_t y = 1; y < terminal_rows; y++)
        for(uint32_t x = 0; x < terminal_columns; x++)
            put_character_at(get_character(x, y), terminal_color, y - 1, x);

    for(uint32_t y = terminal_rows - 1; y < terminal_rows; y++)
        for(uint32_t x = 0; x < terminal_columns; x++)
            put_character_at(' ', terminal_color, y, x);
}

void terminal_change_color()
{
    terminal_color += 10000;
}


void terminal_clear()
{
    for(uint32_t y = 0; y < terminal_rows; y++)
        for(uint32_t x = 0; x < terminal_columns; x++)
            put_character_at(' ', terminal_color, y, x);

    terminal_row = 0;
    terminal_column = 0;
}